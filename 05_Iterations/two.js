
// while loop
let index = 0
while (index <= 10) {
    console.log(`${index}`);
    index =+ 2
}

let myArray = ["f","b","s"]
let i = 0
while (i < myArray.length) {
    console.log(`${myArray[i]}`);
    i +=1
}

//do while
let score = 1
do {
    console.log(`${score}`);
    score ++
} while (score <=  10);